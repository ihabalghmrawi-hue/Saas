import { z } from 'zod'

export const QuotationLineSchema = z.object({
  item_id: z.string().uuid().optional(),
  item_code: z.string().max(50).optional(),
  item_name: z.string().max(200).optional(),
  description: z.string().max(1000).optional(),
  qty: z.number().positive('الكمية يجب أن تكون أكبر من صفر'),
  unit: z.string().default('piece'),
  unit_price: z.number().min(0, 'السعر يجب أن يكون أكبر من أو يساوي صفر'),
  discount_percent: z.number().min(0).max(100).optional(),
  tax_rate: z.number().min(0).optional(),
  warehouse_id: z.string().uuid().optional(),
  delivery_date: z.string().optional(),
})

export const CreateQuotationSchema = z.object({
  customer_id: z.string().uuid('العميل مطلوب'),
  customer_name: z.string().max(200).optional(),
  customer_tax_no: z.string().max(50).optional(),
  branch_id: z.string().uuid().optional(),
  cost_center_id: z.string().uuid().optional(),
  currency: z.string().default('SAR'),
  valid_until: z.string().optional(),
  notes: z.string().max(2000).optional(),
  terms: z.string().max(2000).optional(),
  created_by: z.string().uuid().optional(),
  metadata: z.record(z.unknown()).optional(),
  lines: z.array(QuotationLineSchema).min(1, 'يجب إضافة بند واحد على الأقل'),
})

